#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		count;
	t_list	*tmp;

	count = 0;
	tmp = (t_list *)lst;
	while (tmp)
	{
		count++;
		tmp = tmp->next;
	}
	return (count);
}
